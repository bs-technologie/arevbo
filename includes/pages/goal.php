<h3>Nos Objectifs</h3>
<p>
L’AREVBO a pour objectifs :
</p>
<p>
-	Créer et entretenir entre les membres, un esprit de solidarité et de fraternité ;
</p>
<p>
-	Lutter de toutes ses forces pour assurer la liaison entre ses membres, afin de maintenir l’esprit d’unité;
</p>
<p>
-	Étudier et promouvoir les intérêts collectifs de ses membres;
</p>
<p>
-	Participer de manière effective au processus de développement économique et social de la ville de Bongouanou
</p>
<p>
-	S’ouvrir aux autres groupements ou associations de la région poursuivants les mêmes objectifs;
</p>