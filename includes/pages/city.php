<p>
Le département de Bongouanou a été créé en 1980 comme une scission du département de Dimbokro. En utilisant les limites actuelles comme référence, de 1980 à 2009, le département a occupé le même territoire que la région de Moronou.
</p>
<p>
En 1997, les régions ont été introduites en tant que nouvelles subdivisions de premier niveau de la Côte d'Ivoire; en conséquence, tous les départements ont été convertis en subdivisions de deuxième niveau. Le département de Bongouanou a été inclus dans la région de N'Zi-Comoé.
</p>
<p>
En 2009, le département de Bongouanou a été scindé en trois afin de créer le département d'Arrah et le département de M'Batto.
</p>
<p>
En 2011, les districts ont été introduits en tant que nouvelles subdivisions de premier niveau de la Côte d'Ivoire. Dans le même temps, les régions ont été réorganisées et sont devenues des subdivisions de deuxième niveau et tous les départements ont été convertis en subdivisions de troisième niveau. Depuis lors, le département de Bongouanou au district des Lacs. En 2012, il a été associé aux départements d'Arrah et de M'Batto pour former la nouvelle région du Moronou.
</p>