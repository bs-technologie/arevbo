<p>
L’Association des REssortissants de la Ville BOngouanou (AREVBO) a été créée le 01 Juin 1986. Elle est régie par la loi N 60-315 du 21 septembre 1960 relative aux associations.
Son siège est fixé à Bongouanou. Mais il peut être transféré en cas de besoin en tout autre lieu du territoire national sur décision de l’assemblée générale.
Les réunions de l’AREVBO se tiennent les premiers dimanches de chaque mois. 
</p>
<p>
L’association est subdivisée en 3 sections :
</p>
<p>
-	Abidjan-Nord qui regroupe les communes de Cocody, Plateau, Adjamé, Attécoubé et Abobo ;
</p>
<p>
-	Abidjan-Sud qui regroupent les communes de Treichville, Marcory, Koumassi, Port-Bouët et Grand-Bassam ;
</p>
<p>
-	Yopougon
</p>
<p>
Ces sections se réunissent le dernier week-end de chaque mois pour discuter des questions traitées au cours de la précédente réunion plénière et faire des propositions à examiner pendant la prochaine réunion plénière.
</p>
